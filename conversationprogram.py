gram = int(input("Enter gram: "))
KG = gram / 1000
MilliG = gram * 1000
MicroG = gram * 1000000
Pound = gram * 453.6
Ounce = gram / 28.35
Liter = gram / 1000

print(gram, " gram to Kilogram = ", KG)
print(gram, " gram to Milligram = ", MilliG)
print(gram, " gram to Microgram = ", MicroG)
print(gram, " gram to Pound = ", Pound)
print(gram, " gram to Ounce = ", Ounce)
print(gram, " gram to Liter = ", Liter)
